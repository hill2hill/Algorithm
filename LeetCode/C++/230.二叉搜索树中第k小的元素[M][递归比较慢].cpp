// @lc code=start
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
private:
    vector<int> preOrder;
public:
    
    int kthSmallest(TreeNode* root, int k) {
        //vector<int> preOrder;
        pre(root);
        return preOrder[k-1];
    }
    void pre(TreeNode* root){
        if(!root)   return;
        pre(root -> left);
        preOrder.push_back(root -> val);
        pre(root -> right);
    }
};

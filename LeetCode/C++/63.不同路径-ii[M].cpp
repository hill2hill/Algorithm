// @lc code=start
#include <vector>
using namespace std;
class Solution {
public:
    int uniquePathsWithObstacles(vector<vector<int>>& obstacleGrid) {
        int m = obstacleGrid.size(), n = obstacleGrid[0].size();
        if(obstacleGrid[m-1][n-1] == 1) return 0;//终点无法到达

        vector<long> down_elements(n,0), right_elements(m,0);
        //设置下边界条件
        for(int i = n-1; i >= 0; i--){
            if(!obstacleGrid[m-1][i])   down_elements[i] = 1;
            else break;
        }
        if(m == 1)  return down_elements[0];
        //设置右边界条件
        for(int i = m-1; i>=0; i--){
            if(!obstacleGrid[i][n-1])   right_elements[i] = 1;
            else break;
        }
        
        for(int row = m-2; row >= 0; row--){
            down_elements[n-1] = right_elements[row];
            for(int i = n-2; i >= 0; i--){
                if(obstacleGrid[row][i] == 1)   down_elements[i] = 0; 
                else down_elements[i] += down_elements[i+1]; 
            }
        }
        
        return down_elements[0];
    }
};
